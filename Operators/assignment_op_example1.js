// Use +=, -=, *=, and /= to update a variable a = 5.

let a=5;
// Addition assignment (+=)
a+=3;
console.log(a);
//Subtraction assignment (-=)
a-=3;
console.log(a)
// Subtraction assignment (-=)
a -= 2; 
console.log(a);  

// Multiplication assignment (*=)
a *= 4; 
console.log(a);  

// Division assignment (/=)
a /= 3; 
console.log(a);  
